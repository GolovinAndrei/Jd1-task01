package by.stech.les9.text;

public class Sentence {

	
	private String type;
	private String sent;

	public Sentence() {
	}

	public Sentence(String type) {

	
		this.type = type;
	}

	


	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSent() {
		return sent;
	}

	public void setSent(String sent) {
		this.sent = sent;
	}

}
